# Example
CONST_MATEMATICA = 5
current_nr = 1
# To display 4 we can do
print(CONST_MATEMATICA - current_nr)
# To display 30 we can do
print((CONST_MATEMATICA + current_nr) * CONST_MATEMATICA)
#To display 15 we can do
print(CONST_MATEMATICA + CONST_MATEMATICA + CONST_MATEMATICA)
#To display 16 we can do
print(CONST_MATEMATICA + CONST_MATEMATICA + CONST_MATEMATICA + current_nr)
#To display 20 we can do
print(CONST_MATEMATICA * CONST_MATEMATICA - CONST_MATEMATICA)
#To display 24 we can do
print(CONST_MATEMATICA * CONST_MATEMATICA - current_nr)